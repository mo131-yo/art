const bySlug = new Map(movements.map((m) => [m.slug, m]));

export function getMovement(slug: string): Movement | undefined {
  return bySlug.get(slug);
}

/** Зураачийн чөлөөт бичвэрт нэрлэгдсэн бүх хөдөлгөөн (тодорхойгоос ерөнхий рүү) */
export function movementsOf(artist: Artist): Movement[] {
  const found = movements.filter((m) => m.match.some((k) => artist.movement.includes(k)));

  // «Неоимпрессионизм» олдвол «Импрессионизм»-ыг давхар тоолохгүй
  const specific = new Set(found.map((m) => m.slug));
  return found.filter(
    (m) =>
      !(m.slug === "impressionism" && (specific.has("neo-impressionism") || specific.has("post-impressionism"))) &&
      !(m.slug === "renaissance" && specific.has("northern-renaissance")),
  );
}

export function artistsOf(slug: string): Artist[] {
  return artists.filter((a) => movementsOf(a).some((m) => m.slug === slug));
}

/** Зураачтай хөдөлгөөнүүд, он цагийн дарааллаар */
export function populatedMovements(): (Movement & { artists: Artist[] })[] {
  return movements
    .map((m) => ({ ...m, artists: artistsOf(m.slug) }))
    .filter((m) => m.artists.length > 0);
}
